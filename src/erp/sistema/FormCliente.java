    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nombre =     jTextField1.getText();
        String nit = jTextField2.getText();
        String direccion = jTextField3.getText();
        String telefono = jTextField4.getText();
        String correo = jTextField5.getText();
        int estado = 0;
          boolean isChecked = jCheckBox1.isSelected();
                if (isChecked) {
                    estado = 1;
                } else {
                    estado = 0;
                }

        if (nombre != null && conn != null) {
           
                   
                   cliente.setNombre(nombre);
                   cliente.setNit(nit);
                   cliente.setDireccion(direccion);
                   cliente.setTelefono(telefono);
                   cliente.setCorreo(correo);
                   cliente.setEstado(estado);
              
                   Cliente cl = cliente.crearCliente(conn,cliente);
                   if (cl != null ){
                       
                       JOptionPane.showMessageDialog(null, "Se creo el cliente:" + cl.getNombre());

                  
        
                          limpiar();
                           
                   } else {
                       JOptionPane.showMessageDialog(null, "Error al crear la cliente.");
                   }       
                 
            
                 
        }
    }//GEN-LAST:event_jButton2ActionPerformed

