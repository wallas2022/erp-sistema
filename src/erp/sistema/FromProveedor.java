    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre =     jTextField1.getText();
        String nit = jTextField5.getText();
        String direccion = jTextField2.getText();
        String telefono = jTextField3.getText();
        String correo = jTextField4.getText();
        int estado = 0;
          boolean isChecked = jCheckBox1.isSelected();
                if (isChecked) {
                    estado = 1;
                } else {
                    estado = 0;
                }

        if (nombre != null && conn != null) {
           
                   
                   proveedor.setNombre(nombre);
                   proveedor.setNit(nit);
                   proveedor.setDireccion(direccion);
                   proveedor.setTelefono(telefono);
                   proveedor.setCorreo(correo);
                   proveedor.setEstado(estado);
              
                   Proveedor p = proveedor.crearProveedor(conn,proveedor);
                   if (p != null ){
                       
                       JOptionPane.showMessageDialog(null, "Se creo el proveedor:" + p.getNombre());

                  
        
                          limpiar();
                           
                   } else {
                       JOptionPane.showMessageDialog(null, "Error al crear la categor√≠a.");
                   }       
                 
            
                 
        }
    }//GEN-LAST:event_jButton1ActionPerformed

